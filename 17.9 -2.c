// Program for data type
#include<stdio.h>
int main()
{
    // variable declaration
    int n1;
    int n2;
    float n3;
    double n4;
    char ch;

    // Variable initialization
    n1 = 10;
    n2 = 30;
    n3 = 3.1416;
    n4 = 1.58987;
    ch = 'a';

    // showing result

    printf("%d\n\a",n1);
    printf("%d\n",n2);
    printf("%f\n",n3);
    printf("%lf\n",n4);
    printf("%ch\n",ch);
    return 0;
}
